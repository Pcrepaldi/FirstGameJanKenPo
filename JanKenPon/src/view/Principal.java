    private void jButtonSetarPedraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSetarPedraActionPerformed
        ImageIcon imgtesoura = new ImageIcon("./src/imagens/tesoura.png");
        ImageIcon imgpapel = new ImageIcon("./src/imagens/papel.png");
        ImageIcon imgpedra = new ImageIcon("./src/imagens/pedra.png");
        jLabelJogador.setIcon(imgpedra);
        
        int randomnumber = (int) ( 1 + Math.random() * (4-1));
        
        if(randomnumber == 1){
            jLabelMaquina.setIcon(imgpedra);
            jLabelMAQUINA.setForeground(Color.black);
            jLabelJOGADOR.setForeground(Color.black);
        }
        if(randomnumber == 2){
            jLabelMaquina.setIcon(imgpapel);
            contMaquina++;
            jLabelMAQUINA.setForeground(Color.green);
            jLabelJOGADOR.setForeground(Color.red);
        }
        if(randomnumber == 3){
            jLabelMaquina.setIcon(imgtesoura);
            contJogador++;
            jLabelMAQUINA.setForeground(Color.red);
            jLabelJOGADOR.setForeground(Color.green);
        }
        
        
        
        jLabelPlacarMaquina.setText(Integer.toString(contMaquina));
        jLabelPlacarJogador.setText(Integer.toString(contJogador));
    }//GEN-LAST:event_jButtonSetarPedraActionPerformed

